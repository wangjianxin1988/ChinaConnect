def load_existing(filename):
    """Load existing city JSON and return (metadata, attractions_list)."""
    path = os.path.join(CITY_DIR, filename)
    with open(path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    meta = {k: v for k, v in data.items() if k != 'attractions'}
    existing_ids = {a['id'] for a in data.get('attractions', [])}
    return meta, data.get('attractions', []), existing_ids


def save_city(filename, meta, attractions):
    """Save city JSON file."""
    meta['attractions'] = attractions
    path = os.path.join(CITY_DIR, filename)
    with open(path, 'w', encoding='utf-8') as f:
        json.dump(meta, f, ensure_ascii=False, indent=2)
    print(f"Saved {filename}: {len(attractions)} attractions")


