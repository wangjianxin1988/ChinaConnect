/**
 * D-tier city generation utilities
 * D-tier cities are generated on-demand from templates
 * They use simplified data structures with basic information
 */

// D-tier cities use simplified data with minimal required fields
export interface D_TIER_CITY_DATA {
  name: string;
  nameEn: string;
  slug: string;
  tier: CityTier;
  region: string;
  province: string;
  population: string;
  description: string;
}

/**
 * Generates a D-tier city slug from name
 */
export function generateCitySlug(name: string): string {
  return name.toLowerCase().replace(/\s+/g, "-");
}

/**
 * Generates a D-tier city description based on its characteristics
 */
export function generateCityDescription(template: {
  name: string;
  region: string;
  province: string;
  tags: string[];
}): string {
  const tagStr = template.tags.slice(0, 3).join(", ");
  return `${template.name} is a city in ${template.province} province, ${template.region} China. Known for ${tagStr}. Part of China's extensive city network offering authentic local experiences.`;
}

/**
 * D-tier city data generation function
 * Returns a simplified city object for cities without full data files
 */
export function generateDTierCity(
  template: Omit<A_TIER_CITY_TEMPLATE, "climate" | "cuisine">,
): D_TIER_CITY_DATA {
  return {
    name: template.name,
    nameEn: template.nameEn,
    slug: generateCitySlug(template.name),
    tier: "D",
    region: template.region,
    province: template.province,
    population: template.population,
    description: generateCityDescription(template),
  };
}

/**
 * Gets all S-tier city slugs
 */
export function getSTierCitySlugs(): string[] {
  return Object.keys(S_TIER_CITIES);
}

/**
 * Checks if a city slug is S-tier
 */
export function isSTierCity(slug: string): boolean {
  return slug in S_TIER_CITIES;
}

/**
 * Gets the tier metadata for a city
 */
export function getCityTierMeta(slug: string): CityTierMeta | undefined {
  return S_TIER_CITIES[slug];
}

