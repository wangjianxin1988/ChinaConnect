// Helper function to get restaurants by tier
export function getRestaurantsByTier(tier: FoodTier): Restaurant[] {
  return SAMPLE_RESTAURANTS.filter((r) => r.tier === tier);
}

// Helper function to get restaurants by city
export function getRestaurantsByCity(citySlug: string): Restaurant[] {
  const cityMap: Record<string, string> = {
    shanghai: "上海",
    beijing: "北京",
    chengdu: "成都",
    chongqing: "重庆",
    hangzhou: "杭州",
    guangzhou: "广州",
  };
  const cityName = cityMap[citySlug.toLowerCase()];
  if (!cityName) return [];
  return SAMPLE_RESTAURANTS.filter((r) => r.city === cityName);
}

// Helper function to get tier display info
export function getTierDisplay(tier: FoodTier): { label: string; labelZh: string; icon: string } {
  const tierInfo = {
    S: { label: "Michelin", labelZh: "米其林", icon: "⭐" },
    A: { label: "Black Pearl", labelZh: "黑珍珠", icon: "💎" },
    B: { label: "Local Favorite", labelZh: "本地博主", icon: "🔥" },
  };
  return tierInfo[tier];
}
