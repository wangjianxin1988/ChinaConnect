// Helper to get food data by city ID
export const getFoodDataByCity = (cityId: string): Restaurant[] => {
  return allCityFoodData.find((c) => c.cityId === cityId)?.restaurants ?? [];
};

// Helper to get all budget local restaurants for a city
export const getBudgetLocalByCity = (cityId: string): Restaurant[] => {
  return getFoodDataByCity(cityId).filter((r) => r.type === "budget_local");
};

// Helper to get all hole in the wall restaurants for a city
export const getHoleInWallByCity = (cityId: string): Restaurant[] => {
  return getFoodDataByCity(cityId).filter((r) => r.type === "hole_in_wall");
};

// Helper to get all night market restaurants for a city
export const getNightMarketByCity = (cityId: string): Restaurant[] => {
  return getFoodDataByCity(cityId).filter((r) => r.type === "night_market");
};
